/*
    Write a program that converts 27° from degrees Fahrenheit (F)
    to degrees Celsius (C) using the following formula:

    C = (F - 32) / 1.8
*/

#include <stdio.h>

int main (void)
{
    float C;
    int F = 27;

    printf ("27° degrees Fahrenheit are %.2f degrees Celsius\n",
             C = (F - 32) / 1.8);

    return 0;
}
